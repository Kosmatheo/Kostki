public class Kostki {
    private int nrKostki;
    private int wartoscostki;

    public Kostki(int nrKostki, int wartoscostki) {
        this.nrKostki = nrKostki;
        this.wartoscostki = wartoscostki;
    }

    public int getNrKostki() {
        return nrKostki;
    }

    public void setNrKostki(int nrKostki) {
        this.nrKostki = nrKostki;
    }

    public int getWartoscostki() {
        return wartoscostki;
    }

    public void setWartoscostki(int wartoscostki) {
        this.wartoscostki = wartoscostki;
    }
}
